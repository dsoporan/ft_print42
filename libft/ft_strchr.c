#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	tg;

	i = 0;
	tg = (char)c;
	while (s[i] && s[i] != c)
		i++;
	if ((s[i] == '\0' && tg == '\0') || s[i] == c)
		return ((char *)(s + i));
	return (NULL);
}
